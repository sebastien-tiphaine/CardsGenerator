CardsStore::storeCard('MS-ChordArpeggio3to9-IIm7',
	function($oGen, $strMajorScalePosCat, $arrCallBackConditions, $strGroupId, $intScalePos){
		CardSkeleton::getSkeleton('MS-ChordArpeggio3to9')->insertAsTemplate(
			/* Generator */ $oGen,
			/* Values */   	array(
								CardGenerator::CARDCATEGORY 	 => array($strMajorScalePosCat, MAJOR_SCALE),
								CardGenerator::CALLBACKCONDITION => array($arrCallBackConditions),
								CardGenerator::CARDDISPLAYCATEGORY => false
							),
			/* Variables */ array(
								'groupId' 	    => $strGroupId,
								'scalePos'      => $intScalePos,
								'chord'         => 2,
								'chordQuality'  => 'm7',
								'chordSub'		  => 4, 
								'chordSubQuality' => '7M',
								'imageExt' 		=> 'IIm7',
								'arpNote1' 		=> 4,
								'arpNote2' 		=> 6,
								'arpNote3' 		=> 1,
								'arpNote4' 		=> 3,
							)
		);
});

